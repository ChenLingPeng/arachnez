object TimeoutFuture {
  def apply[A](timeout: FiniteDuration)(block: => A): Future[A] = {
    val prom = Promise[A]()

    Akka.system().scheduler.scheduleOnce(timeout) {
      prom tryFailure new TimeoutException
    }

    Future {
      prom success block
    }

    prom.future
  }
}
